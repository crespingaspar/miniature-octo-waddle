<script>
	import wPicker from "@/components/w-picker/w-picker.vue";
	import xiaEditor from '@/components/xia-editor/xia-editor';
	import multipleSelect from "@/components/momo-multipleSelect/momo-multipleSelect";
	export default {
		data() {
			return {
				cross:'',
				ruleForm: {
				kechengmingcheng: '',
				sectionnum: '',
				sectionnums: [],
				riqi: '',
				week: '',
				banji: '',
				jiaoshi: '',
				gonghao: '',
				jiaoshixingming: '',
				},
				sectionnumOptions: [],
				sectionnumShow: false,
				riqistarttime: '',
				// 登录用户信息
				user: {},
				ro:{
				   kechengmingcheng : false,
				   sectionnum : false,
				   riqi : false,
				   week : false,
				   banji : false,
				   jiaoshi : false,
				   gonghao : false,
				   jiaoshixingming : false,
				},
				virtualPay: false,
				showType: 1,
			}
		},
		components: {
			wPicker,
			xiaEditor,
			multipleSelect,
		},
		computed: {
			baseUrl() {
				return this.$base.url;
			},
			sessionForm() {
				return uni.getStorageSync("appUserid")?JSON.parse(uni.getStorageSync('userSession')):{}
			},



		},
		async onLoad(options) {
			if(options.virtualPay){
				this.virtualPay = true
			}
			this.riqistarttime = this.$utils.getCurDate()
			let table = uni.getStorageSync("nowTable");
			// 获取用户信息
			let res = await this.$api.session(table);
			this.user = res.data;
			
			// ss读取
			this.ruleForm.gonghao = this.user.gonghao
			this.ro.gonghao = true;
			this.ruleForm.jiaoshixingming = this.user.jiaoshixingming
			this.ro.jiaoshixingming = true;

			// 跨表
			this.cross = options.cross;
			if(options.cross){
				var obj = uni.getStorageSync('crossObj');
				for (var o in obj){
					if(o=='kechengmingcheng'){
						this.ruleForm.kechengmingcheng = obj[o];
						this.ro.kechengmingcheng = true;
						continue;
					}
					if(o=='sectionnum'){
						this.ruleForm.sectionnum = obj[o];
						this.ro.sectionnum = true;
						continue;
					}
					if(o=='riqi'){
						this.ruleForm.riqi = obj[o];
						this.ro.riqi = true;
						continue;
					}
					if(o=='week'){
						this.ruleForm.week = obj[o];
						this.ro.week = true;
						continue;
					}
					if(o=='banji'){
						this.ruleForm.banji = obj[o];
						this.ro.banji = true;
						continue;
					}
					if(o=='jiaoshi'){
						this.ruleForm.jiaoshi = obj[o];
						this.ro.jiaoshi = true;
						continue;
					}
					if(o=='gonghao'){
						this.ruleForm.gonghao = obj[o];
						this.ro.gonghao = true;
						continue;
					}
					if(o=='jiaoshixingming'){
						this.ruleForm.jiaoshixingming = obj[o];
						this.ro.jiaoshixingming = true;
						continue;
					}
				}
			}

			var sectionnumRefParams = {}
			res = await this.$api.option(`scheduletime`,`sectionnum`,sectionnumRefParams);
			let sectionnumArr = []
			for (let x in res.data) {
				sectionnumArr.push({
					label: res.data[x],
					value: res.data[x]
				})
			}
			this.sectionnumOptions = sectionnumArr

			// 如果有登录，获取登录后保存的userid
			this.ruleForm.userid = uni.getStorageSync("appUserid")
			if (options.refid) {
				// 如果上一级页面传递了refid，获取改refid数据信息
				this.ruleForm.refid = Number(options.refid);
				this.ruleForm.nickname = uni.getStorageSync("nickname");
			}
			// 如果是更新操作
			if (options.id) {
				this.ruleForm.id = options.id;
				// 获取信息
				res = await this.$api.info(`kebiaoxinix`, this.ruleForm.id);
				if (res.data.sectionnum) {
					let brr = res.data.sectionnum.split(',')
					res.data.sectionnums = []
					brr.forEach(item => {
						for (let a in this.sectionnumOptions) {
							if (this.sectionnumOptions[a].label == item) {
								res.data.sectionnums.push(this.sectionnumOptions[a].value)
							}
						}
					})
				}
				this.ruleForm = res.data;
			}
			this.$forceUpdate()
			if (uni.getStorageSync('raffleType') && uni.getStorageSync('raffleType') != null) {
				uni.removeStorageSync('raffleType')
				setTimeout(() => {
					this.onSubmitTap(null)
				}, 300)
			}
		},
		methods: {
			numberChange(e,name) {
				this.ruleForm[name] = Number(e.detail.value)
			},
			sectionnumShowPicker() {
				this.sectionnumShow = true
			},
			sectionnumConfirm(e) {
				this.ruleForm.sectionnums = e
				let arr = []
				for (let x in this.ruleForm.sectionnums) {
					arr.push(this.ruleForm.sectionnums[x].label)
				}
				this.ruleForm.sectionnum = arr.join(',')
				this.$forceUpdate()
			},

			// 多级联动参数

			riqiChange(e) {
				this.ruleForm.riqi = e.target.value;
				this.$forceUpdate();
			},

			// 日长控件选择日期时间
			riqiConfirm(val) {
				this.ruleForm.riqi = val.result;
				this.$forceUpdate();
			},



			getUUID () {
				return new Date().getTime();
			},
			async onSubmitTap(subMitType=null) {
				let that = this
				//跨表计算判断
				var obj;
				if((!this.ruleForm.kechengmingcheng)){
					this.$utils.msg(`课程名称不能为空`);
					return
				}
				if((!this.ruleForm.sectionnum)){
					this.$utils.msg(`节数不能为空`);
					return
				}
				if((!this.ruleForm.riqi)){
					this.$utils.msg(`日期不能为空`);
					return
				}
				if((!this.ruleForm.week)){
					this.$utils.msg(`星期不能为空`);
					return
				}
				if(this.ruleForm.week&&(!this.$validate.isIntNumer(this.ruleForm.week))){
					this.$utils.msg(`星期应输入整数`);
					return
				}
				if((!this.ruleForm.banji)){
					this.$utils.msg(`班级不能为空`);
					return
				}
				if((!this.ruleForm.jiaoshi)){
					this.$utils.msg(`教室不能为空`);
					return
				}
				//更新跨表属性
				var crossuserid;
				var crossrefid;
				var crossoptnum;
				if(this.cross){
					var statusColumnName = uni.getStorageSync('statusColumnName');
					var statusColumnValue = uni.getStorageSync('statusColumnValue');
					if(statusColumnName!='') {
						if(!obj) {
							obj = uni.getStorageSync('crossObj');
						}
						if(!statusColumnName.startsWith("[")) {
							for (var o in obj){
								if(o==statusColumnName){
									obj[o] = statusColumnValue;
								}
							}
							var table = uni.getStorageSync('crossTable');
							await this.$api.update(`${table}`, obj);
						} else {
								crossuserid=Number(uni.getStorageSync('appUserid'));
								crossrefid=obj['id'];
								crossoptnum=uni.getStorageSync('statusColumnName');
								crossoptnum=crossoptnum.replace(/\[/,"").replace(/\]/,"");
						}
					}
					if(crossrefid && crossuserid) {
						this.ruleForm.crossuserid=crossuserid;
						this.ruleForm.crossrefid=crossrefid;
						let params = {
							page: 1,
							limit:10,
							crossuserid:crossuserid,
							crossrefid:crossrefid,
						}
						let res = await this.$api.list(`kebiaoxinix`, params);
						if (res.data.total >= crossoptnum) {
							this.$utils.msg(uni.getStorageSync('tips'));
							uni.removeStorageSync('crossCleanType');
							return false;
						}
					}
					//跨表计算
				}
				let oet = {}
				if(this.ruleForm.id){
					await this.$api.update(`kebiaoxinix`, this.ruleForm);
				}else{
					oet = await this.$api.add(`kebiaoxinix`, this.ruleForm);
				}
				if(this.cross){
					uni.setStorageSync('crossCleanType',true);
				}
				that.$utils.msgBack('提交成功');
			},
			optionsChange(e) {
				this.index = e.target.value
			},
			bindDateChange(e) {
				this.date = e.target.value
			},
			getDate(type) {
				const date = new Date();
				let year = date.getFullYear();
				let month = date.getMonth() + 1;
				let day = date.getDate();
				if (type === 'start') {
					year = year - 60;
				} else if (type === 'end') {
					year = year + 2;
				}
				month = month > 9 ? month : '0' + month;;
				day = day > 9 ? day : '0' + day;
				return `${year}-${month}-${day}`;
			},
			toggleTab(str) {
				if(this.ro[str]){
					return false
				}
				this.$refs[str].show();
			},
		}
	}
</script>

